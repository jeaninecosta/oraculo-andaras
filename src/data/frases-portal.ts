export function getFraseParaTema(pergunta: string): string {
  const p = pergunta.toLowerCase()
  let tema = 'geral'
  if (/amor|relacionamento|parceiro|namorad|casamento|afeto|coração|saudade/.test(p)) tema = 'amor'
  else if (/cura|dor|doença|saúde|saude|sofr|difícil|dificil|triste|ansied|medo/.test(p)) tema = 'cura'
  else if (/propósito|proposito|missão|missao|carreira|trabalho|vocação|vocacao|dom|talento/.test(p)) tema = 'proposito'
  else if (/decidir|decisão|decisao|escolha|escolher|devo|caminho|direção|direcao/.test(p)) tema = 'decisao'
  else if (/espiritu|deus|universo|alma|sagrado|fé|fe|oração|oracao|medita/.test(p)) tema = 'espiritualidade'
  const frases = frasesPortal[tema]
  return frases[Math.floor(Math.random() * frases.length)]
}
